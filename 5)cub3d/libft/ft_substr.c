#include "libft.h"

char	*ft_substr(char const *s, unsigned int start, size_t len)
{
	char	*sub;
	size_t	size;

	if (!s)
		return (NULL);
	if (len > ft_strlen(s))
		len = ft_strlen(s);
	sub = (char *)malloc(len + 1);
	if (!sub)
		return (NULL);
	size = ft_strlen(s);
	if ((size_t)start >= size)
		sub[0] = 0;
	else
		ft_strlcpy(sub, s + start, len + 1);
	return (sub);
}
